import java.util.*;

public class Note{
    private String title;
    private String body;


    //Constructor 
    public Note(String title, String body){

    }
    
    //Getters
    public String getTitle(){
        return title;
    }

    public String getBody(){
        return body;
    }

    //Setters 
    public void setTitle(String title){
        this.title = title;
    }

    public void setBody(String body){
        this.body = body;
    }
}


